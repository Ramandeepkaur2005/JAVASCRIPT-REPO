//declaring variables using 'let'
let fullname="Ramandeep kaur";
console.log(fullname);
let age="18";
console.log(age);
let birthyear="2005";
console.log(birthyear);

//declaring variable using 'var'
var x = 98;
var y = "change";
console.log(x);
console.log(y);

//declaring variable using  'const'
const pi = 3.14;
//pi = 2.22;------>This will show an error
console.log(pi);


//variables declared with var are in the function scope.
//variables declared with let are in the block scope.
//variables declared with const are in the block scope.